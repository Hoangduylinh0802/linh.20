CREATE TABLE NhanVien(
MSNV VARCHAR(5) NOT NULL PRIMARY KEY, 
HOTEN NVARCHAR(20) NOT NULL,
GIOITINH NVARCHAR(10) NOT NULL,
DIACHI NVARCHAR(50) NOT NULL,
LUONG DECIMAL(10,2) NOT NULL
)
go
CREATE TABLE HoSo(
MSHS VARCHAR(10) NOT NULL PRIMARY KEY,
MSNV VARCHAR(5) NOT NULL ,
TENHS NVARCHAR(20) NOT NULL,
NgayLapHS NVARCHAR(20) NOT NULL,
SLHS decimal(10,2) NOT NULL,
CONSTRAINT fk_HS_NV
FOREIGN KEY (MSNV)
REFERENCES NhanVien (MSNV)
)
go
CREATE TABLE Xe(
MSXE VARCHAR(10) NOT NULL PRIMARY KEY,
MSNV VARCHAR(5) NOT NULL ,
TENXE NVARCHAR(20) NOT NULL,
TENHANGXE NVARCHAR(20) NOT NULL,
SLXE decimal(10,2) NOT NULL,
SLĐCHUAN decimal(10,2) NOT NULL
CONSTRAINT fk_XE_NV
FOREIGN KEY (MSNV)
REFERENCES NhanVien (MSNV)
)
go
CREATE TABLE ViPham(
MSVIPHAM VARCHAR(10) NOT NULL PRIMARY KEY,
MSHS VARCHAR(10) NOT NULL,
TENVIPHAM NVARCHAR(50) NOT NULL,
SLXEVIPHAM decimal(10,2) NOT NULL,
SOTIENPHAT MONEY
CONSTRAINT fk_HS_VP
FOREIGN KEY (MSHS)
REFERENCES HoSo (MSHS)
)
go
